namespace KeyValuePair
{
    /// <summary>
    /// Definition for the sort order of the list.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// No specific sort order.
        /// </summary>
        NONE,

        /// <summary>
        /// Sort by name.
        /// </summary>
        NAME, 

        /// <summary>
        /// Sort by value.
        /// </summary>
        VALUE
    }
}